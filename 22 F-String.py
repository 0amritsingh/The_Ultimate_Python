# F-String in Python

greeting = "Hello!"
name = "Amrit"
age = 16
location = "Uttrakhand"

print(f"{greeting} my name is {name}. I'm {age} and I live in {location}")

# So, yeah this is f-string. Let's see another example

earning = 49.83754
print(f"Hey! I earned {earning} today.")
print(f"Hey! I earned {earning:.2f} today.") # using this cammand you can print only two dicemal points.
