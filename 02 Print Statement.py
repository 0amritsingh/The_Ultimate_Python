# The Print Statement 

# we can write string and integers in print statement
# Strint: alphabets can be writen in colons ( "" or '' )
# Integer: integers can be writen as it is, if integers writen in colons it is a string
print("Hello, World!")
print(1)
# we can write multiple string and integers in print statement
print("Hello,", "World!")
print(1, 2)
# we can also use operators to solve equtions 
print(1+2)
print(1-2)
print(1*2)
print(1/2)