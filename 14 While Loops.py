# While Loops in Python:

# Positive Looping:
i = 0
while (i <= 5):
    print(i)
    i = i + 1 # or i += 1

print("\n")
# Negitive Looping:
j = 5
while (j >= 0):
    print(j)
    j = j - 1 # or i -= 1

print("\n")
# Using Else in While Loop:
k = 0
while (k < 5):
    print(k)
    k = k + 1
else:
    print(5) # this will run only with the condition is FALSE.  
    
